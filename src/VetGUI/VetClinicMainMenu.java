    private void btn_DeletePetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletePetActionPerformed
        int i = petsTable.getSelectedRow();
        if(i == -1){
            JOptionPane.showMessageDialog(this, "No pet selected yet!");           
        }
        else{
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure to delete this? ");
            if(dialogResult == JOptionPane.YES_OPTION){
                petTableModel.deletePet(i);
            }   
        }
        
    }//GEN-LAST:event_btn_DeletePetActionPerformed

    private void btn_EditPetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditPetActionPerformed
        int i = petsTable.getSelectedRow();
        if(i == -1){
            JOptionPane.showMessageDialog(this, "No pet selected yet!");           
        }
        else{
            editPet.setEditPet(petTableModel.getSelectedPet(i));
            editPet.setVisible(true);
            if(editPet.getDoneEditPet() != null){
                petTableModel.updatePet(editPet.getDoneEditPet());
                JOptionPane.showMessageDialog(this, "Pet Successfully updated!");
            }else{
                JOptionPane.showMessageDialog(this, "Pet is not updated!");
            }
        }
    }//GEN-LAST:event_btn_EditPetActionPerformed

