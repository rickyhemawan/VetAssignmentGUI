    private void btn_addDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addDrugActionPerformed
        // TODO add your handling code here:
        //create add drug dialog
        // getpet from table
        //create dose with specs
        //date today date
        // check dose null or not
        // if not null pet add dose
        //last update to table
        int i = petsTable.getSelectedRow();
        if(i == -1){
            JOptionPane.showMessageDialog(this, "No pet selected yet!");           
        }
        else{
            Pet pet = petTableModel.getSelectedPet(i);
            if (pet.getDose() == 0){
                JOptionPane.showMessageDialog(this, "Cant add another drug to this pet");
            }
            else{
                addPetDrug.setPet(pet);
                addPetDrug.setVisible(true); 
            }
            if(addPetDrug.getPet() == null && addPetDrug.getDose() == null){
                JOptionPane.showMessageDialog(this, "Drug addition cancelled.");
            }
            else{
                petTableModel.updatePet(addPetDrug.getPet());
                JOptionPane.showMessageDialog(this, "Drug added successfully");
            }
            addPetDrug.setPet(null);
            addPetDrug.setDose(null);
        }
        
        
    }//GEN-LAST:event_btn_addDrugActionPerformed

    private void btn_showDrugsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_showDrugsActionPerformed
        int i = petsTable.getSelectedRow();
        if(i == -1){
            JOptionPane.showMessageDialog(this, "No pet selected yet!");           
        }
        else{
            Pet pet = petTableModel.getSelectedPet(i);
            showDrugs.setDoses(pet.getDoseList());
            if(pet.getDoseList().isEmpty()){
                JOptionPane.showMessageDialog(this, "No Drug is registered to this pet yet!");
            }
            else{
                showDrugs.setVisible(true);
            }
        }
    }//GEN-LAST:event_btn_showDrugsActionPerformed

